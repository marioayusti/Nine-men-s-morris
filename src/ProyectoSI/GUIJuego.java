    public void colocarFicha(boolean turnoJ1, int idPunto) {
        this.addLog("Jugador " + (turnoJ1 ? 1 : 2) + " - Coloca una ficha en el punto " + idPunto);
        this.panel_Juego1.colocarFicha(idPunto);
    }

